    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
          try {

            Conexion conexion = new Conexion();
            Connection con = conexion.obtenerConexion();
//            Class.forName("com.mysql.jdbc.Driver").newInstance();
//            /***realizar la conexion a la BD***/
//            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/conexion", "root", "admin");
            System.out.println("Coneccion exitosa");
            
            Statement stm = con.createStatement();
            ResultSet rst = stm.executeQuery("SELECT * FROM idconeccion");
            ResultSetMetaData rsMD = rst.getMetaData();
            
            int numeroColumnas = rsMD.getColumnCount();
            
            DefaultTableModel modelo = new DefaultTableModel(); //coneccion entre tabla y datos
            
            this.jTable1.setModel(modelo);
            
              for (int i = 1; i <= numeroColumnas ; i++) {
                  modelo.addColumn(rsMD.getColumnLabel(i));
              }
              
              while (rst.next()) {                  
                 Object fila[] = new Object[numeroColumnas]; //debido a que tengo "int y string"
                 
                  for (int j = 0; j < numeroColumnas; j++) {
                   fila[j]= rst.getObject(j+1);
                  }
                  modelo.addRow(fila);   
              }
              
              this.jTable1.setModel(modelo);

        }catch(SQLException e){
        
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

