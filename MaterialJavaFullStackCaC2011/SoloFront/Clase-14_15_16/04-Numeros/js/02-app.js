const numero1 = 30;
const numero2 = 20;


let resultado;

// Suma
resultado = numero1 + numero2;
console.log(resultado);

// Resta
resultado = numero1 - numero2;
console.log(resultado);
// Multiplicación
resultado = numero1 * numero2;
console.log(resultado);
// División
resultado = numero1 / numero2;
console.log(resultado);
// Modulo
resultado = numero1 % numero2;

console.log(resultado);